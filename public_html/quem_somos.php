<h5>Composição</h5>
<h6>História</h6>
Nesta parte, você contará sobre a criação e progresso da empresa, até os dias atuais.<br>

Ex: fundada em 1900, estamos no mercado de [segmento] há XX anos…

<h6>Diferenciais</h6>
O que sua empresa oferece de diferencial. Ex: no atendimento, nos produtos e serviços, na entrega, no pagamento.<br>

Seja breve, claro e sucinto quanto às características que diferenciam seu negócio dos concorrentes.<br>

<h6>Missão</h6>

Responde às perguntas mais fundamentais de todas:<br>

<p>Porque sua empresa existe?
Qual seu propósito maior?
O que sua empresa faz?
Para quem ela faz? A quem ela se destina?
Exemplo: Missão do Google: Organizar a informação mundial para torná-la universalmente acessível e útil.</p>

<p>A missão precisa ser original e representar a personalidade da sua marca, fazer sentido a longo prazo e ser memorável.</p>

<p>Quanto mais simples e objetiva, melhor.</p>

<h6>Visão</h6>

<p>É importante estabelecer a perspectiva de longo prazo. Inspira o que se almeja para o futuro da empresa.</p>

<p>É uma das partes mais importantes, pois o objetivo principal é criar uma ideia de futuro do seu negócio — mesmo objetivo desta mentoria.</p>

<p><b>Exemplo:</b> Visão da Nike: Ajudar a empresa Nike e nossos consumidores a prosperarem em uma economia sustentável onde pessoas, lucros e planetas estão em equilíbrio.</p>

<p>Outro exemplo: Visão da Unilever: Nossa visão é aumentar nossos negócios, enquanto desassociamos o crescimento da pegada ambiental e aumentamos o impacto social positivo.</p>

<p>Uma visão que faça sentido precisa visualizar a situação da empresa em 5 anos, ser escrita no futuro, mostrar a direção a ser seguida, ser ambiciosa e ousada e mostrar qual o impacto que essa direção irá causar no mundo.</p>

<h6>Valores</h6>

<p>Missão e visão são as rotas de execução do planejamento estratégico do seu negócio.</p>

<p>Os valores guiam sua conduta nesse processo.
Devem refletir princípios éticos e morais, unindo o plano pessoal ao plano empresarial.
São princípios inegociáveis que ditam comportamentos e atitudes no funcionamento de toda a estrutura organizacional.
Se você acordasse amanhã e o dinheiro não fosse mais uma necessidade pro resto da sua vida, como você se manteria firme a esses valores?</p>

<p>Eles ainda fariam sentido pra você ou pro seu negócio? Esses valores serão válidos para daqui há 100 anos?</p>

<p>Mesmo que em algum momento eles se tornem uma desvantagem competitiva ou que você iniciasse outra empresa em outro segmento, você construiria essa nova empresa baseada nesses valores?</p>

